#include "LED.h"
#include "Std_Types.h"
#include "SysTick.h"
#include "IntCtrl.h"
#include "Dio.h"
#include "Port.h"

/**********************************************************************************************************************
 *  GLOBAL DATA
 *********************************************************************************************************************/
void LED_Init(void)
{
		Port_Init(Ports_Config);
}

void LED_ON(void)
{
		Dio_WriteChannel(PORTF, PIN1, High);
}

void LED_OFF(void)
{
		Dio_WriteChannel(PORTF, PIN1, Low);
}

void LED_TOGGLE(void)
{
		Dio_FlipChannel(PORTF, PIN1);
}

Dio_LevelType LED_STATUS(void)
{
		return Dio_ReadChannel(PORTF, PIN1);
}

